function Slider_dt(e, tab_dt) {
    for(var i = 0; i < $(".chuyenslide_dt").length; i++) {
        $(".chuyenslide_dt")[i].style.display = 'none';
    }
    $(".chuyenslide_dt")[tab_dt - 1].style.display = 'block';
}
