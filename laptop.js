function Slider_lt(e, tab_lt) {
    for(var i = 0; i< $(".chuyenslide_lt").length; i++) {
        $('.chuyenslide_lt')[i].style.display = 'none';
    }
    $(".chuyenslide_lt")[tab_lt - 1].style.display = 'block';
}
